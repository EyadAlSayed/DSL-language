package Models.ControllerModels.Action;

public class MathEquation {

    Sum sum;
    Mult mult;
    Div div;
    Minus minus;
    Mod mod;

    public Sum getSum() {
        return sum;
    }

    public void setSum(Sum sum) {
        this.sum = sum;
    }

    public Mult getMult() {
        return mult;
    }

    public void setMult(Mult mult) {
        this.mult = mult;
    }

    public Div getDiv() {
        return div;
    }

    public void setDiv(Div div) {
        this.div = div;
    }

    public Minus getMinus() {
        return minus;
    }

    public void setMinus(Minus minus) {
        this.minus = minus;
    }

    public Mod getMod() {
        return mod;
    }

    public void setMod(Mod mod) {
        this.mod = mod;
    }
}
